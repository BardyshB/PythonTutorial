"""
Python Primitive Data Types
"""

"""
INTEGER EXAMPLE
"""
i1 = 1
print(i1)

i10 = 10
print(i10)

"""
FLOAT EXAMPLE
"""
f1 = 1.0
print(f1)

f2 = 2.0
print(f2)

f0_2 = .2
print(f0_2)

"""
COMPLEX EXAMPLE
"""
a = 2 + 3j
print('a =', a)
print('Type of a is', type(a))

b = -2j
print('b =', b)
print('Type of b is', type(a))

c = 0j
print('c =', c)
print('Type of c is', type(c))

c23 = complex(2, -3)
print(c23)

c1 = complex(1)
print(c1)

c0 = complex()
print(c0)

c59 = complex('5-9j')
print(c59)

"""
STRING EXAMPLE
"""
string1 = 'this is string'
print(string1)

string2 = "this is also string"
print(string2)

"""
BOOLEAN EXAMPLE
"""

true = True
false = False
